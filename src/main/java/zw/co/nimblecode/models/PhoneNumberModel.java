package zw.co.nimblecode.models;

public class PhoneNumberModel {
    private String code;
    private String digits;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDigits() {
        return digits;
    }

    public void setDigits(String digits) {
        this.digits = digits;
    }
}
